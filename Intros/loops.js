// For loops 
/*
initializer = starting of the counting 
for (initializer; condition; final-expression){
    code-block
}
*/

for (let i = 0; i <= 10; i++){
    //console.log(i);
} // 0 - 10

for(let x = 10; x > 0; x--){
    //console.log(x);
} // 10 - 0


// While loop
/*
while(condition){
    code-block
}
*/

let y = 0;
while ( y < 11){
    //console.log(y);
    y++
}

let z = 10;
while ( z > -1){
    //console.log(z);
    z--
}


// Do while loop
/*
do {
    code-block
} while (condition);
*/
let j = 10;
do {
    console.log("peace");
    j++
} while (j < 20);